public class Variaveis {

    public static void main(String[] args) {
        System.out.println("Variáveis e Tipos de Dados");

        int idade = 19;
        float numero = 1.2f;
        double ponto_decimal = 25.50;
        char simbolo = '$';
        boolean VouF = true;

        System.out.println("idade: " + idade);
        System.out.println("Número float: " + numero);
        System.out.println("Ponto decimal: " + ponto_decimal);
        System.out.println("Símbolo: " + simbolo);
        System.out.println("Valor Booleano: " + VouF);
    }
}
